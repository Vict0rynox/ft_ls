#include "libft.h"

int	ft_isnum(char *str)
{
	char	*ptr_str;

	ptr_str = str;
	while (*ptr_str != '\0')
	{
		if (*ptr_str < '0' || *ptr_str > '9')
			return (0);
		ptr_str++;
	}
	return (1);
}

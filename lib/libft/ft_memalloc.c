#include "libft.h"

void	*ft_memalloc(size_t size)
{
	unsigned char *mem;

	mem = (unsigned char *)malloc(size);
	if (mem != NULL)
		mem = ft_memset(mem, 0, size);
	return ((void *)mem);
}
